    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        ControlTienda obja = new ControlTienda();
        
        listaTienda = obja.consultarTiendas();

        for (int i = 0; i < listaTienda.size(); i++) {
            Tienda objetoClasificacion = listaTienda.get(i);
            jComboBox1.addItem(String.valueOf(objetoClasificacion.getIdTienda()));
        }

        ControlTipoProducto objcpo=new ControlTipoProducto();
        listaTipo=objcpo.consultarTipoProducto();
        for (int j = 0; j < listaTipo.size(); j++) {
            TipoProducto objpais = listaTipo.get(j);
            
            jComboBox2.addItem(String.valueOf(objpais.getIdTipo()));
        }
    }//GEN-LAST:event_formWindowOpened

